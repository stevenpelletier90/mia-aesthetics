			<p>
				Because every person is different, every lower body lift may be slightly different.
				The procedure generally follows these basic steps, however:
			</p>
			<ol class="how-it-works-list">
				<li>
				Your surgical team will prepare you for surgery and then place you under general
				anesthesia.
				</li>
				<li>
				Your procedure will begin with your surgeon making an incision around the entire
				circumference of your body. You can think of this incision wrapping around you like
				a belt.
				</li>
				<li>The surgeon will cut away any excess skin and fat as necessary.</li>
				<li>
				After the excess skin and fat are removed, your surgeon will fine tune the remaining
				tissue, tightening the muscles as necessary to strengthen and smooth the remaining
				structures.
				</li>
				<li>
				Your surgeon will lift the remaining skin and pull it taut over your new body
				contours before suturing the incision closed.
				</li>
				<li>
				Your surgical team will bandage your incisions as needed and place you in a
				compression garment. They will then wake you from anesthesia and take you to the
				recovery room for monitoring as you wake up.
				</li>
				<li>
				When you have sufficiently awoken from anesthesia, your driver can take you home.
				Lower body lifts are performed as outpatient procedures at Mia Aesthetics.
				</li>
