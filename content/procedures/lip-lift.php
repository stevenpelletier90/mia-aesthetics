          <div class="col-md-6">
            <h3 class="fw-bold mb-3">Bullhorn (Subnasal) Lip Lift</h3>
            <p class="mb-0">
              During this type of lift, your plastic surgeon will make an incision directly under your nose in the shape of a bull's horns. This is the most common and versatile type of lip lift, and
              it hides any post-op scarring beneath the nose. Plastic surgeons use the bullhorn lift to elevate the center of the upper lip, maintain a natural lip curvature, and increase the amount
              of tooth surface that shows when you speak and smile.
            </p>
          </div>
          <div class="col-md-6">
            <h3 class="fw-bold mb-3">Corner Lip Lift</h3>
            <p class="mb-0">
              Another common type of lip lift, the corner lift, is often combined with the bullhorn lift. Focusing on lifting the outer corners of your lips, this lift creates a softer, more upturned
              expression. If the corners of your lips turn down, or if your mouth always looks sad, the corner lip lift may work well for you.
            </p>
          </div>
        </div>
