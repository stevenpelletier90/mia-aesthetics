			This is used for breast lifts that require only subtle adjustments.
			</p>
			<p>
			For more extensive lifts, an incision extending down from the areola may be added. For
			very extensive lifts, another incision may be added underneath the breasts, close to the
			breast crease. Once the incisions are made, excess skin will be removed and tightened,
			and the breast will be contoured and supported for a lifted, perky, shapely appearance.
			</p>
