          </p>
          <p class="mb-3">
            The shape of the nose is altered by either removing or adding cartilage to the inside of the nose or reshaping the nasal bones. If cartilage must be added, the graft is most commonly taken
            from the middle of the nose (the septum), but it may also be taken from the ear if the nasal cartilage is not available. In rare cases, the cartilage may be taken from the rib. Taking
            these grafts may require separate incisions and anesthesia. The surgical plan is made in advance so that patients are aware of all the steps that will take place during the procedure.
          </p>
          <p class="mb-0">After the interior sculpting of the nose structure is complete, the incisions are closed. All of these steps can be done in a single procedure.</p>
