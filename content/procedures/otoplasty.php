            <h2 class="display-6 fw-bold">About the Ear Pinning Procedure</h2>
          </div>
          <p class="mb-3">
            Although the exact surgical technique used will depend on several factors, the basics of otoplasty remain the same: First, incisions are made in the ear and used to remove extra fat and
            skin. Second, the cartilage is contoured and positioned for optimal aesthetics. And lastly, the incisions are closed with stitches.
          </p>
          <p class="mb-3">
            In most cases, the surgery is performed while the patient is under general anesthesia. When ear surgery is performed on children, the ear cartilage is very soft, allowing for contouring
            with splints. Adults have much harder cartilage, so the procedure will consist of repositioning or removing cartilage through a small ear incision.
          </p>
          <p class="mb-0">
            Depending on your desired changes and unique anatomy, your surgeon may decide to make an incision hidden behind the ear. During the procedure, extra soft tissue or cartilage that causes
            the ear to stick out can be removed. If the ear is missing its normal folds, your doctor can shape the cartilage and create these folds. In many situations, the folds can be created with
            permanent sutures, or the cartilage can be contoured through targeted shaving. Depending on the desired result and the initial complications, your surgeon might use multiple techniques.
          </p>
        </div>
