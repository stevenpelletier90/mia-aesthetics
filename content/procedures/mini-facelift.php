			</div>

			<div class="fixed-bg-content-box p-0 p-lg-4">
			<p class="mb-4">
				We know that although surgery is just part of the daily routine for us, it's anything but ordinary for you. Many of our patients express feelings of nervousness and anxiety prior to
				their procedures, and we do what we can to put them at ease. One of the best ways to do this is by making sure that you know what to expect when you come in for your procedure. To that
				end, here are the steps involved in a mini facelift:
			</p>
			<ol class="mb-4">
				<li class="mb-3">
				The first step for our patients at Mia Aesthetics is always a free virtual consultation. During this consultation we will assess whether you are a candidate for a mini facelift and
				answer any questions you may have about getting one.
				</li>
				<li class="mb-3">
				When you arrive on the day of your surgery, you'll be prepped for surgery and have a quick check-in with your surgeon. After that, we'll take you into the OR and place you under
				general anesthesia.
				</li>
				<li class="mb-3">
				Once you're peacefully sleeping, your surgeon will start the procedure by making their incisions. For a mini facelift, doctors usually hide their incisions along the natural crease at
				the front of your ear. The cut may continue down around and behind the earlobe, if necessary, but it will still be well hidden.
				</li>
				<li class="mb-3">
				Next your plastic surgeon will gently separate your skin from the underlying tissue and muscles. They will then tighten these muscles as needed to create the desired shape and contour.
				</li>
				<li class="mb-3">Your surgeon will then cut away any sagging, stretched, or excess skin.</li>
				<li class="mb-3">The remaining skin is smoothed over the lower portion of your face and pulled taut.</li>
				<li class="mb-4">Lastly, your incisions will be closed and any necessary dressings applied.</li>
			</ol>
			<p class="mb-3">
				Your care team will monitor you as you wake up from the anesthesia and then send you home the same day as your surgery. Your surgeon will prescribe pain medication to keep you
				comfortable, but discomfort is generally mild after a mini facelift. You will manage swelling and bruising by applying icepacks to your face. You will also need to keep your incision
				sites clean and dry as they heal.
			</p>
			<p class="mb-0">
				Your surgeon will schedule a follow-up appointment with you a few days after your surgery. It's important that you keep this appointment. It is when your stitches will be removed, if
				applicable, and allows your surgeon to check your progress and make sure you are healing well and without complications.
			</p>
