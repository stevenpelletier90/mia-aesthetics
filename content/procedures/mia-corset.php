	<div class="row align-items-center">
		<div class="col-lg-6 order-2 order-lg-2">
		<div class="content-wrapper">
			<h2>How Rib Repositioning Works</h2>
			<p>
			Performed as an outpatient surgery, rib remodeling changes the position of your bottom
			two ribs. These ribs are called floating ribs because they aren't attached to your
			sternum like your other ribs.
			</p>
			<p>
			During the procedure, your surgeon will access these ribs through small incisions that
			are about the same size as liposuction incisions (two to three mm). As always, they will
			hide these incisions in the natural contours of the body as much as possible. Through
			these incisions your surgeon will carefully dissect the muscles and soft tissue to
			expose the ribs.
			</p>
			<p>
			Once they have exposed them, surgeons gently reposition the ribs to reduce rib flare and
			slim the waistline. This involves surgically fracturing the ribs so they can be
			relocated. Fracturing is done in a very precise and controlled manner to produce the
			desired outcome without damaging any underlying structures.
			</p>
			<p>
			After repositioning the ribs, your surgeon may use sutures, wires, or other devices as
			needed to hold the ribs in place as they heal. These fixation methods are typically made
			from materials that will dissolve and be absorbed by the body over time.
			</p>
		</div>
